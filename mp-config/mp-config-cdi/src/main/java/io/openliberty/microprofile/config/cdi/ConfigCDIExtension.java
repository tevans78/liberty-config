package io.openliberty.microprofile.config.cdi;

import jakarta.enterprise.event.Observes;
import jakarta.enterprise.inject.spi.AfterBeanDiscovery;
import jakarta.enterprise.inject.spi.BeanManager;
import jakarta.enterprise.inject.spi.Extension;

/**
 *
 */
public class ConfigCDIExtension implements Extension {

    void processInjectionTarget(@Observes AfterBeanDiscovery abd, BeanManager beanManager) {
        abd.addBean(new ConfigBean(beanManager));
    }

}
